import Foundation
import UIKit

extension UIColor {
    
    public class var fundRedColor: UIColor {
        return UIColor(red: 236/255.0, green: 0/255.0, blue: 68/255.0, alpha: 1)
    }
    public class var fundGreenColor: UIColor {
        return UIColor(red: 108/255.0, green: 192/255.0, blue: 73/255.0, alpha: 1)
    }
    public class var fundBlueColor: UIColor {
        return UIColor(red: 0/255.0, green: 117/255.0, blue: 201/255.0, alpha: 1)
    }
    public class var fundYellowColor: UIColor {
        return UIColor(red: 255, green: 206, blue: 0, alpha: 1)
    }
}
